public class Exo1{
    public static void main(String[] args) {
        int i = 2;
        int res = i++;

        System.out.println(res);     //resultat = 2
        System.out.println(i);      //resultat = 3

        /*
        Explication :
            Post-Incrementation (i++) :
            → On utilise d'abord la valuer de  i, Apres on ajouter +1

         */
    }
}